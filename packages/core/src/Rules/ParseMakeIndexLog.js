/* @flow */

import Rule from '../Rule'

import type { Action, Command, ParsedLog } from '../types'

const MESSAGE_PATTERN = /^\s+--\s*(.*)$/

export default class ParseMakeIndexLog extends Rule {
  static parameterTypes: Array<Set<string>> = [new Set(['MakeIndexLog'])]
  static commands: Set<Command> = new Set(['build', 'log'])
  static defaultActions: Array<Action> = ['parse']
  static description: string = 'Parses any logs generated by makeindex.'

  async parse () {
    const output = await this.getResolvedOutput('$FILEPATH_0-ParsedMakeIndexLog')
    if (!output) return false

    const name = 'makeindex'
    const parsedLog: ParsedLog = {
      messages: [],
      inputs: [],
      outputs: [],
      calls: [],
      requests: []
    }

    await this.firstParameter.parse([{
      names: ['input'],
      patterns: [
        /^Scanning (?:style|input) file (.*?)[.]+done .*$/
      ],
      evaluate: (reference, groups) => {
        parsedLog.inputs.push(this.normalizePath(groups.input))
        parsedLog.messages.push({
          severity: 'info',
          name,
          text: groups._,
          log: reference
        })
      }
    }, {
      names: ['output'],
      patterns: [
        /^(?:Output|Transcript) written in (.*?)\.*$/
      ],
      evaluate: (reference, groups) => {
        parsedLog.outputs.push(this.normalizePath(groups.output))
        parsedLog.messages.push({
          severity: 'info',
          name,
          text: groups._,
          log: reference
        })
      }
    }, {
      names: ['text'],
      patterns: [
        /^(Nothing written in .*?\.)$/
      ],
      evaluate: (reference, groups) => {
        parsedLog.messages.push({
          severity: 'warning',
          name,
          text: groups.text,
          log: reference
        })
      }
    }, {
      names: ['text'],
      patterns: [
        /^(Sorting entries.*)$/
      ],
      evaluate: (reference, groups) => {
        parsedLog.messages.push({
          severity: 'info',
          name,
          text: groups.text,
          log: reference
        })
      }
    }, {
      names: ['inputPath', 'inputLine', 'outputPath', 'outputLine', 'text'],
      patterns: [
        /## Warning \(input = (.+), line = (\d+); output = (.+), line = (\d+)\):/,
        MESSAGE_PATTERN
      ],
      evaluate: (reference, groups) => {
        const line = parseInt(groups.inputLine, 10)
        parsedLog.messages.push({
          severity: 'warning',
          name: 'makeindex',
          text: groups.text,
          log: reference,
          source: {
            file: groups.inputPath,
            range: {
              start: line,
              end: line
            }
          }
        })
      }
    }, {
      names: ['category', 'file', 'line', 'text'],
      patterns: [
        /^[*!]+ (Input (?:index|style)) error \(file = (.+), line = (\d+)\):$/,
        MESSAGE_PATTERN
      ],
      evaluate: (reference, groups) => {
        const line = parseInt(groups.line, 10)
        parsedLog.messages.push({
          severity: 'error',
          name: 'makeindex',
          text: groups.text,
          category: groups.category,
          log: reference,
          source: {
            file: groups.file,
            range: {
              start: line,
              end: line
            }
          }
        })
      }
    }])

    output.value = parsedLog

    return true
  }
}
